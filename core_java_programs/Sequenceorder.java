package core_java_programs;

public class Sequenceorder {
	public static void main(String[]args) {
		String str = "Mintu";
		for(int i=0;i<str.length();i++) {
			str.charAt(i);
			System.out.println(str.charAt(i));
		}
		for(int j=0;j<str.length();j++) {
			str.charAt(j);
//			System.out.print(str.charAt(j)+ " ");
			System.out.print(str.charAt(j));
			
		}

	}
	
}
